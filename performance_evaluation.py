import matplotlib.pyplot as plt
from sklearn.datasets import load_iris
from sklearn.neighbors import KNeighborsClassifier
from sklearn.model_selection import train_test_split
from sklearn.metrics import (confusion_matrix, classification_report,
                             roc_curve, auc, ConfusionMatrixDisplay)
from sklearn.preprocessing import label_binarize

#Loading the data and splitting
iris = load_iris()
X_train, X_test, y_train, y_test = train_test_split(iris.data, iris.target, test_size=0.3, random_state=42)

#Implementation of a kNN classifier
knn_eval = KNeighborsClassifier(n_neighbors=5)
knn_eval.fit(X_train, y_train)

#Predict results for X_test data
y_pred = knn_eval.predict(X_test)
y_score = knn_eval.predict_proba(X_test) # Probability scores for ROC/AUC
#Confusion Matrix results
print("Confusion Matrix:")
cm = confusion_matrix(y_test, y_pred)
print(cm)

# ConfusionMatrix
disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=iris.target_names)
disp.plot(cmap=plt.cm.Blues) # Using the Blue colormap
plt.title("Confusion Matrix (k=5)")
plt.show()

# Accuracy, Precision, Recall, and F1 score results
print("\nClassification Report:")
print(classification_report(y_test, y_pred, target_names=iris.target_names))

#ROC analysis
y_test_bin = label_binarize(y_test, classes=[0, 1, 2])
n_classes = 3

#  ROC curve
plt.figure(figsize=(8, 6))
for i in range(n_classes):
    fpr, tpr, _ = roc_curve(y_test_bin[:, i], y_score[:, i])
    roc_auc = auc(fpr, tpr)
    plt.plot(fpr, tpr, label=f'ROC curve of {iris.target_names[i]} (AUC = {roc_auc:0.2f})')


plt.plot([0, 1], [0, 1], 'k--', lw=2) # Diagonal black dashed line
plt.xlim([0.0, 1.0])
plt.ylim([0.0, 1.05])
plt.xlabel('False Positive Rate')
plt.ylabel('True Positive Rate')
plt.title('Receiver Operating Characteristic (ROC) - Multi-class')
plt.legend(loc="lower right")
plt.show()